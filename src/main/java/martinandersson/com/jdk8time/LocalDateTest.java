    public static void main(String... ignored) {
        
        // Monday, February 29 is a leap day in 2016 (otherwise, February only has 28)
        LocalDate start = parse("2016-02-28"),
                  end   = parse("2016-03-02");
        
        // 4 days between (end is inclusive): 02-28, 02-29, 03-01 and 03-02.
        
        Stream.iterate(start, date -> date.plusDays(1))
                .limit(ChronoUnit.DAYS.between(start, end) + 1)
                .forEach(System.out::println);
        
        System.out.println();
        
        LocalDate next = start.minusDays(1);
        while ((next = next.plusDays(1)).isBefore(end.plusDays(1))) {
            System.out.println(next);
        }
        
        
        
        // Comparable
        List<LocalDate> dates = Arrays.stream(DATA.split("\\n"))
                .map(LocalDate::parse)
                .collect(toList());
        
        System.out.println("Number of dates: " + dates.size());
        
        Collections.shuffle(dates);
        LocalDate[] minMax = findMinMaxDate(dates.iterator());
        
        System.out.println("1 Min: " + minMax[0]);
        System.out.println("1 Max: " + minMax[1]);
        
        Collections.shuffle(dates);
        minMax = findMinMaxDate(dates.stream().iterator());
        
        System.out.println("2 Min: " + minMax[0]);
        System.out.println("2 Max: " + minMax[1]);
        
        Collections.shuffle(dates);
        Collections.sort(dates);
        
        System.out.println("3 Min: " + dates.get(0));
        System.out.println("3 Max: " + dates.get(dates.size() - 1));
    }
    
    private static LocalDate[] findMinMaxDate(Iterator<LocalDate> dateIt) {
        LocalDate min = LocalDate.MAX,
                  max = LocalDate.MIN;
        
        Set<LocalDate> saw = new HashSet<>();
        int count = 0;
        
        while (dateIt.hasNext()) {
            LocalDate x = dateIt.next();
            saw.add(x);
            ++count;
            
            if (x.compareTo(min) < 0) {
                min = x;
            }
            
            if (x.compareTo(max) > 0) {
                max = x;
            }
        }
        
        if (count == 0) {
            throw new IllegalArgumentException("Dates is empty.");
        }
        
        System.out.println("Count: " + count);
        System.out.println("Unique: " + saw.size());
        
        return new LocalDate[]{ min, max };
    }
    
